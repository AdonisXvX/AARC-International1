        // Get current language from localStorage or default to French
        let currentLang = localStorage.getItem('language') || 'fr';

        // Function to change language
        function changeLanguage(lang) {
            currentLang = lang;
            localStorage.setItem('language', lang);
            
            // Update flag active state
            document.querySelectorAll('.lang-switcher img').forEach(img => {
                img.classList.remove('active-lang');
                if (img.alt.toLowerCase() === lang) {
                    img.classList.add('active-lang');
                }
            });
            
            // Translate all elements
            translatePage();
        }

        // Function to translate the page
        function translatePage() {
            const elements = document.querySelectorAll('[data-translate]');
            elements.forEach(element => {
                const key = element.getAttribute('data-translate');
                if (translations[currentLang][key]) {
                    element.textContent = translations[currentLang][key];
                }
            });
            
            // Translate placeholders
            const placeholders = document.querySelectorAll('[data-translate-placeholder]');
            placeholders.forEach(element => {
                const key = element.getAttribute('data-translate-placeholder');
                if (translations[currentLang][key]) {
                    element.placeholder = translations[currentLang][key];
                }
            });
        }

        // Initialize language on page load
        window.addEventListener('DOMContentLoaded', () => {
            // Set active flag
            document.querySelectorAll('.lang-switcher img').forEach(img => {
                if (img.alt.toLowerCase() === currentLang) {
                    img.classList.add('active-lang');
                }
            });
            
            // Translate page
            translatePage();
        });

        // Hero Carousel functionality
        let currentSlide = 0;
        const slides = document.querySelectorAll('.carousel-slide');
        let autoSlideInterval;

        function showSlide(n) {
            slides.forEach(slide => slide.classList.remove('active'));
            currentSlide = (n + slides.length) % slides.length;
            slides[currentSlide].classList.add('active');
        }

        function changeSlide(n) {
            showSlide(currentSlide + n);
            resetAutoSlide();
        }

        function autoSlide() {
            changeSlide(1);
        }

        function resetAutoSlide() {
            clearInterval(autoSlideInterval);
            autoSlideInterval = setInterval(autoSlide, 3000);
        }

        // Start auto-sliding
        autoSlideInterval = setInterval(autoSlide, 3000);

